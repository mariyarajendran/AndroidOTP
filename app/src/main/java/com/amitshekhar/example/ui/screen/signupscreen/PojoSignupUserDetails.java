package com.amitshekhar.example.ui.screen.signupscreen;

public class PojoSignupUserDetails {

    String user_id;
    String user_name;
    String user_mailid;
    String user_mobile_number;
    String user_current_lat;
    String user_current_lng;
    String user_otp;

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getUser_mailid() {
        return user_mailid;
    }

    public void setUser_mailid(String user_mailid) {
        this.user_mailid = user_mailid;
    }

    public String getUser_current_lat() {
        return user_current_lat;
    }

    public void setUser_current_lat(String user_current_lat) {
        this.user_current_lat = user_current_lat;
    }

    public String getUser_current_lng() {
        return user_current_lng;
    }

    public void setUser_current_lng(String user_current_lng) {
        this.user_current_lng = user_current_lng;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }


    public String getUser_mobile_number() {
        return user_mobile_number;
    }

    public void setUser_mobile_number(String user_mobile_number) {
        this.user_mobile_number = user_mobile_number;
    }

    public String getUser_otp() {
        return user_otp;
    }

    public void setUser_otp(String user_otp) {
        this.user_otp = user_otp;
    }


}
